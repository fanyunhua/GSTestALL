package cn.pfc.pfc523.beans;

public class T35Bean {
    int img;
    String place;
    int price;

    public T35Bean(int img, String place, int price) {
        this.img = img;
        this.place = place;
        this.price = price;
    }

    public int getImg() {
        return img;
    }

    public String getPlace() {
        return place;
    }

    public int getPrice() {
        return price;
    }
}
